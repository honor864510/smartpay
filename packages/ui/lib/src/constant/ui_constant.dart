/// A utility class that holds constant values for UI elements.
/// This class cannot be instantiated and only provides static constants.
final class UiConstant {
  const UiConstant._();

  /// The default border radius value for UI elements.
  /// Value: 12.0
  static const borderRadius = 12.0;

  /// A smaller border radius value for UI elements that need less rounded corners.
  /// Value: 6.0
  static const borderRadiusSmall = 6.0;

  /// A larger border radius value for UI elements that need more rounded corners.
  /// Value: 20.0
  static const borderRadiusLarge = 20.0;

  /// The default padding value for UI elements.
  /// Value: 16.0
  static const padding = 16.0;

  /// A smaller padding value for UI elements that need less space.
  /// Value: 8.0
  static const paddingSmall = 8.0;

  /// A larger padding value for UI elements that need more space.
  /// Value: 24.0
  static const paddingLarge = 24.0;
}
